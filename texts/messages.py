def settings_text() -> str:
    gemini_status = (
        "включено ✅" if settings.GEMINI_ENABLED else "выключено ⛔️ (нет ключа)"
    )
    if settings.access_restricted:
        access_status = f"закрытый 🔒 ({len(settings.allowed_user_ids)} ID)"
    else:
        access_status = "открытый 🌍"
    audio_backend = (
        "fal.ai (Stable Audio 3)"
        if settings.HF_AUDIO_BACKEND == "fal"
        else "hf-inference (legacy)"
    )
    return (
        "⚙️ <b>Настройки</b>\n\n"
        f"Доступ к боту: {access_status}\n"
        f"Генерация аудио: {audio_backend}\n"
        f"Улучшение промтов через Gemini: {gemini_status}\n"
        f"Максимальная длительность джингла: до {settings.MAX_EXTENDED_MUSIC_DURATION_SECONDS:.0f} сек "
        f"(склейка сегментов по {settings.MAX_MUSIC_DURATION_SECONDS:.0f} сек)\n"
        f"Максимальная длительность звукового эффекта: ~{settings.MAX_SOUND_DURATION_SECONDS:.0f} сек\n"
        f"Вариантов на запрос: {settings.GENERATION_VARIANTS}\n"
        f"Авто-полировка: {'вкл ✅' if settings.AUTO_MASTER_POLISH else 'выкл'}\n"
        f"Дневной лимит: {settings.DAILY_GENERATION_LIMIT} генераций\n"
        "Используй /help, чтобы увидеть список всех команд."
    )
